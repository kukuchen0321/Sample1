import java.util.*;

public class Book {
    public String id;
    public String isbn;
    public String name;
    public String author;
    public String public_date;

    public Book(String id, String isbn, String name, String author, String public_date) {
        this.id = id;
        this.isbn = isbn;
        this.name = name;
        this.author = author;
        this.public_date = public_date;
    }


}
